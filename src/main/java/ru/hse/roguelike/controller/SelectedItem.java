package ru.hse.roguelike.controller;

/**
 * Enum class for game menu items.
 **/
public enum SelectedItem {
    /**
     * Start game.
     **/
    START_GAME,
    /**
     * Start game with level generation from file.
     **/
    START_GAME_FROM_FILE,
    /**
     * Show game rules.
     **/
    SHOW_RULES,
    /**
     * Exit game.
     **/
    EXIT
}
