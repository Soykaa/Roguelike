package ru.hse.roguelike.controller.input;

/**
 * Enum class for supported keys.
 **/
public enum InputCommand {
    /**
     * Up arrow key.
     **/
    UP,
    /**
     * Down arrow key.
     **/
    DOWN,
    /**
     * Left arrow key.
     **/
    LEFT,
    /**
     * Right arrow key.
     **/
    RIGHT,
    /**
     * Enter key.
     **/
    ENTER,
    /**
     * Backslash key.
     **/
    BACKSLASH,
    /**
     * Space key.
     **/
    SPACE,
    /**
     * Escape key.
     **/
    ESCAPE,
    /**
     * Unsupported key.
     **/
    UNKNOWN_COMMAND
}
